if HAS_PIL:
    def generate_font_pil():
        """Generate font using Pillow from system TTF font."""
        # Try to find a suitable font on macOS
        font_paths = [
            "/System/Library/Fonts/Supplemental/Courier New.ttf",
            "/System/Library/Fonts/Menlo.ttc",
            "/System/Library/Fonts/Helvetica.ttc",
            None,  # PIL default
        ]
        font = {}
        try:
            for fp in font_paths:
                if fp and os.path.exists(fp):
                    break
            else:
                fp = None

            # Use a large size and scale down for better quality
            try:
                pil_font = ImageFont.truetype(fp or "courier", 20) if fp else ImageFont.load_default()
            except Exception:
                pil_font = ImageFont.load_default()

            for c in range(0x20, 0x7F):
                img = Image.new("1", (8, 16), 0)
                draw = ImageDraw.Draw(img)
                draw.text((0, 0), chr(c), font=pil_font, fill=1)
                pixels = list(img.getdata())
                rows = []
                for y in range(16):
                    byte = 0
                    for x in range(8):
                        if pixels[y * 8 + x]:
                            byte |= (0x80 >> x)
                    rows.append(byte)
                font[c] = rows
        except Exception:
            font = generate_vga_font()
        return font


def generate_vga_font_data():
    """Generate font data for all printable ASCII chars."""
    if HAS_PIL:
        try:
            return generate_font_pil()
        except Exception:
            pass
    return generate_vga_font()


def main():
    outfile = sys.argv[1] if len(sys.argv) > 1 else "font_8x16.h"

    font_data = generate_vga_font_data()

    with open(outfile, "w") as f:
        f.write("/* Auto-generated 8x16 ASCII bitmap font */\n")
        f.write("#ifndef FONT_8X16_H\n#define FONT_8X16_H\n\n")
        f.write("#include <stdint.h>\n\n")

        f.write("/* 8x16 pixel bitmap font for ASCII 0x20-0x7E\n")
        f.write(" * Each character: 16 bytes, 1 byte per row, MSB = leftmost pixel\n")
        f.write(" * Index: (char_code - 0x20) * 16\n")
        f.write(" */\n")
        f.write("static const uint8_t font_8x16[] = {\n")

        for c in range(0x20, 0x7F):
            f.write(f"    /* 0x{c:02X} '{chr(c) if 0x20 <= c < 0x7F else '?'}' */\n")
            rows = font_data.get(c, [0]*16)
            line = "    " + ", ".join(f"0x{b:02X}" for b in rows)
            if c < 0x7E:
                line += ","
            f.write(line + "\n")

        f.write("};\n\n")
        f.write("#define FONT_8X16_CHAR_WIDTH  8\n")
        f.write("#define FONT_8X16_CHAR_HEIGHT 16\n")
        f.write("#define FONT_8X16_FIRST_CHAR  0x20\n")
        f.write("#define FONT_8X16_LAST_CHAR   0x7E\n")
        f.write("#define FONT_8X16_BYTES_PER_CHAR 16\n\n")
        f.write("#endif /* FONT_8X16_H */\n")

    count = sum(1 for c in range(0x20, 0x7F))
    print(f"Generated font data: {count} characters -> {outfile}")


if __name__ == "__main__":
    main()
